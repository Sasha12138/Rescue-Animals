              this.ruleForm.nianling = obj[o];
              this.ro.nianling = true;
              continue;
            }
            if(o=='yanse'){
              this.ruleForm.yanse = obj[o];
              this.ro.yanse = true;
              continue;
            }
            if(o=='tupian'){
              this.ruleForm.tupian = obj[o].split(",")[0];
              this.ro.tupian = true;
              continue;
            }
            if(o=='shentizhuangkuang'){
              this.ruleForm.shentizhuangkuang = obj[o];
